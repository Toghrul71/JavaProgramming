package day18_NestedLoop;

public class NestedLoopPractice2 {
    public static void main(String[] args) {

        for (int j = 0; j < 3; j++) {  // when j=0 there are ten * s printed
            for (int i = 0; i < 10; i++) {
                System.out.print("* ");
        }

            System.out.println();
        }

    }
}
